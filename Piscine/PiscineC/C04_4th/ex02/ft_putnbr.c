#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	num_rev[11];
	int		i;

	i = 0;
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb == 0)
		write(1, "0", 1);
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb *= -1;
		}
		while (nb)
		{
			num_rev[i] = (nb % 10) + 48;
			nb /= 10;
			i++;
		}
		i--;
		while (i >= 0)
			write(1, &num_rev[i--], 1);
	}
}
